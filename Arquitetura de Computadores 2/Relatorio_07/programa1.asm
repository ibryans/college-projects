.text
.globl main
main:
addi $8,$0,0x2;      # $8:  a = 2;
addi $9,$0,0x3;      # $9:  b = 3;
addi $10,$0,0x4;     # $10: c = 4;
addi $11,$0,0x5;     # $11: d = 5;
add $12,$8,$9;      # $12: (a+b) == 5
add $13,$10,$11;    # $13: (c+d) == 9
sub $14,$12,$13;    # $14: x = (a+b)-(c+d) == -4
sub $15,$8,$9;      # $15: y = (a-b) == -1
add $15,$15,$14;    # $15: y = a-b+x == -5
sub $9,$14,$15;     # $9:  b = x - y; == 1

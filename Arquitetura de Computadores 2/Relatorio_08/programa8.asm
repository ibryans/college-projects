.globl main

main:

ori $8, $0, 0x1234    # $8 = 00001234
sll $8, $8, 16        # $8 = 12340000
ori $8, $8, 0x5678    # $8 = 0x12345678

srl $9, $8, 16        # $9 = 00001278
srl $9, $9, 8         # $9 = 00000012 = 0x12

sll $10, $8, 8        # $10 = 34567800
srl $10, $10, 16      # $10 = 00003456
srl $10, $10, 8       # $10 = 00000034 = 0x34

sll $11, $8, 16       # $11 = 56780000
srl $11, $11, 16      # $11 = 00005678
srl $11, $11, 8       # $11 = 00000056 = 0x56

sll $12, $8, 16       # $12 = 56780000
sll $12, $12, 8       # $12 = 78000000
srl $12, $12, 16      # $12 = 00007800
srl $12, $12, 8       # $12 = 00000078 = 0x78